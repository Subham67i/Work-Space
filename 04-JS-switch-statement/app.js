// switch statement -----

let grade = "A";

switch(grade){
    case "A" : 
    console.log('A - very good');
    break;

    case "B" : 
    console.log('B - keep learning');
    break;

    case "C" : 
    console.log('C - need improve');
    break;

    case "D" : 
    console.log('D - not good');
    break;
    default :
    console.log('invalid');
}
